class Tuple{

	//   ---   ---   ---   //
	private int x;
	private int y;
	//   ---   ---   ---   //


	Tuple(int i, int j){

		//   ---   ---   ---   //
		x = i;	y = j;
		//   ---   ---   ---   //

	}


	//   ---   ---   ---   //
	void setX(int i){ x = i; }
	void setY(int j){ x = j; }
	//   ---   ---   ---   //


	//   ---   ---   ---   //
	int getX(){ return x; }
	int getY(){ return y; }
	//   ---   ---   ---   //

}